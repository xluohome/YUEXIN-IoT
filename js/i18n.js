  // ── Public API ───────────────────────────────────────────────
  window.i18n = {
    getLang: function() {
      return localStorage.getItem(LANG_KEY) || DEFAULT_LANG;
    },
    setLang: function(lang) {
      if (!translations[lang]) return;
      localStorage.setItem(LANG_KEY, lang);
      this.apply();
    },
    t: function(key) {
      const lang = this.getLang();
      return (translations[lang] && translations[lang][key]) || key;
    },
    apply: function() {
      const lang = this.getLang();
      document.querySelectorAll('[data-i18n]').forEach(function(el) {
        const key = el.getAttribute('data-i18n');
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = translations[lang] ? translations[lang][key] || el.placeholder : el.placeholder;
        } else {
          el.textContent = translations[lang] ? translations[lang][key] || el.textContent : el.textContent;
        }
      });
      document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
        const key = el.getAttribute('data-i18n-html');
        el.innerHTML = translations[lang] ? translations[lang][key] || el.innerHTML : el.innerHTML;
      });
      // Update lang toggle buttons
      document.querySelectorAll('.lang-btn-en').forEach(function(el) {
        el.classList.toggle('active', lang === 'en');
      });
      document.querySelectorAll('.lang-btn-vi').forEach(function(el) {
        el.classList.toggle('active', lang === 'vi');
      });
      // Update html lang attribute
      document.documentElement.lang = lang;
    }
  };

  // Init on DOM ready
  document.addEventListener('DOMContentLoaded', function() {
    window.i18n.apply();

    // Lang toggle click handlers
    document.querySelectorAll('.lang-btn-en').forEach(function(btn) {
      btn.addEventListener('click', function() { window.i18n.setLang('en'); });
    });
    document.querySelectorAll('.lang-btn-vi').forEach(function(btn) {
      btn.addEventListener('click', function() { window.i18n.setLang('vi'); });
    });
  });
